
/*
Write three functions to calculate the bitwise AND,
bitwise OR and bitwise XOR of two numbers.
*/

function  bitwiseAND(x,y){
    console.log(x&y)
}

bitwiseAND(7, 12) // 4

function bitwiseOR(x,y) {
    console.log(x | y)
}

bitwiseOR(7, 12) // 15

function bitwiseXOR(x,y) {
    console.log(x^y)
}

bitwiseXOR(7, 12) //  11
