
/*
The left shift operation is similar to 
multiplication by powers of two.
*/

function  shiftToLeft(x,y){
    console.log(x<<y)
}


shiftToLeft(5, 2) // 20

shiftToLeft(10, 3) //  80

shiftToLeft(-32, 2) //  -128

shiftToLeft(-6, 5) //  -192

shiftToLeft(12, 4) // 192

shiftToLeft(46, 6) //  2944
